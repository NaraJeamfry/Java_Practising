import java.awt.Graphics;

public abstract class Element {

	private int x, y, width, height;
	
	public Element(int x, int y, int width, int height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
	abstract void draw(Graphics g);
}
